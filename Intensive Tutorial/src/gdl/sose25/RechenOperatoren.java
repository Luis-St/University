package gdl.sose25;

public class RechenOperatoren {
	
	public static void main(String[] args) {
		
		System.out.println(42 + 42);
		
		System.out.println(42 * 42);
		
		System.out.println(42 - 42);
		
		System.out.println(42 * 42 - 42);
		
		System.out.println(42 / 8);
		
		System.out.println(42.0 / 8.0);
	}

}
